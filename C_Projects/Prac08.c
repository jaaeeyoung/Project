#include <stdio.h>
#include <stdbool.h>
#include <stdlib.h>

// 파라미터로 주어지는 문자열은 const로 주어집니다. 변경하려면 문자열을 복사해서 사용하세요.
char *solution(const char *s)
{
    // return 값은 malloc 등 동적 할당을 사용해주세요. 할당 길이는 상황에 맞게 변경해주세요.
    char *answer = (char *)malloc(2);
    int count=0;

    for(int i=0;i<sizeof(s);i++){
        if(s[i]!='\0'){
            count++;
        }
        answer[i] = '\0';
    }
    printf("count = %d\n", count);

    if(count%2==1){ // 글자 수가 홀수이면
        answer[0] = s[count / 2-1]; // 중간 글자 answer에 포함
        printf("answer = %c\n", answer[0]);
    }
    
    else { // 글자 수가 짝수이면
        answer[0] = s[count/2-1]; // 중간 첫 글자 answer에 포함
        printf("answer = %c\n", answer[0]);
        answer[1] = s[count / 2]; // 중간 두 번째 글자 answer에 포함
        printf("answer = %c\n", answer[1]);
    }

    return answer;
}

int main(void)
{
    printf("%s\n", solution("abcde"));
    printf("%s\n", solution("qwer"));
}