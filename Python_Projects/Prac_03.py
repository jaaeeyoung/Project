def solution(n):
    answer = [0] * (3)
    answer[1]=1
    if(n>1):
        for i in range(n):
            answer[2] = answer[0]+answer[1]
            answer[0], answer[1] = answer[1], answer[2]
    return answer[n]

for n in range(11):
    print(solution(n))