def solution(n):
    answer = 0
    nums = []
    for i in range(2, n + 1):
        nums.append(i)

    i = 0
    while i <= len(nums):
        number = 0
        for j in range(nums[i]):
            for k in range(2, j // 2 + 1):
                if j % k == 0:
                    number = j
        
        j = 2
        while number * j <= nums[-1]:
            nums.remove(nums[number * j])
            j += 1

        i += 1
    return answer

