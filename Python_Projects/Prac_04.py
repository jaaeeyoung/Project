'''
=======================================================================================================================================
+ 문제 +
=======================================================================================================================================
2016년 ☆
=======================================================================================================================================
+ 문제 설명 +
=======================================================================================================================================
' 2016년 1월 1일은 금요일입니다. 2016년 a월 b일은 무슨 요일일까요? 두 수 a, b를 입력받아 2016년 a월 b일이 무슨 요일인지 리턴하는 함수,
  solution을 완성하세요. 요일의 이름은 일요일부터 토요일까지 각각 SUN, MON, TUE, WED, THU, FRI, SAT 입니다. 예를 들어 a=5, b=24라면 5월 24일
  은 화요일이므로 문자열 "TUE"를 반환하세요.
=======================================================================================================================================
+ 제한 사항 +
=======================================================================================================================================
' 2016년은 윤년입니다.
' 2016년 a월 b일은 실제로 있는 날입니다.(13월 26일이나 2월 45일같은 날짜는 주어지지 않습니다.)
=======================================================================================================================================
+ 입출력 예 +
=======================================================================================================================================
' a = 5, b = 24, result = "TUE"
' a = 1, b = 31, result = "SUN"
' a = 2, b = 29, result = "MON"
' a = 3, b = 31, result = "THU"
' a = 4, b = 30, result = "SAT"
' a = 5, b = 24, result = "TUE"
' a = 5, b = 31, result = "TUE"
' a = 6, b = 30, result = "THU"
' a = 7, b = 31, result = "SUN"
' a = 8, b = 31, result = "WED"
' a = 9, b = 30, result = "FRI" -> THU
' a = 10, b = 31, result = "MON"
' a = 11, b = 30, result = "WED"
' a = 12, b = 31, result = "SAT"
=======================================================================================================================================
+ 입출력 예 설명 +
=======================================================================================================================================

=======================================================================================================================================
+ 수정 기록 +
=======================================================================================================================================
' 2021-03-19-FRI : 코딩 시작
                   a월 이전 달까지의 날짜 수 count
                   a월 b일까지 날짜 수 count
                   9/30 목요일로 출력되는 오류
=======================================================================================================================================
'''

def solution(a, b):
    answer = ''
    day = ["FRI", "SAT", "SUN", "MON", "TUE", "WED", "THU"]
    count=0

    for i in range(1, a):
        if i==2:
            for i in range(1, 30):
                count+=1
            count%=7

        elif i==1 or i==3 or i==5 or i==7 or i==8 or i==10 or i==12:
            for i in range(1, 32):
                count+=1
            count%=7

        else:
            for i in range(1, 31):
                count+=1
            count%=7

    for i in range(1, b+1):
        count+=1

    count=count%7-1
    if count==-1:
        count=6

    answer=day[count][:]
    return answer

