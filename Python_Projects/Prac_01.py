'''
=======================================================================================================================================
+ 문제 설명 +
=======================================================================================================================================
' 초 단위로 기록된 주식 가격이 담긴 배열 prices가 매개변수로 주어질 때, 가격이 떨어지지 않은 기간은 몇 초인지를 return 하도록 solution 함수를
  완성하세요.
=======================================================================================================================================
+ 제한 사항 +
=======================================================================================================================================
' prices의 각 가격은 1 이상 10,000 이하인 자연수입니다.
' prices의 길이는 2 이상 100,000 이하입니다.
=======================================================================================================================================
+ 입출력 예 +
=======================================================================================================================================
' prices = [1, 2, 3, 2, 3]
' return = [4, 3, 1, 1, 0]
=======================================================================================================================================
+ 입출력 예 설명 +
=======================================================================================================================================
' 1초 시점의 \1은 끝까지 가격이 떨어지지 않았습니다.
' 2초 시점의 \2은 끝까지 가격이 들어지지 않았습니다.
' 3초 시점의 \3은 1초뒤에 가격이 떨어집니다. 따라서 1초간 가격이 떨어지지 않은 것으로 봅니다.
' 4초 시점의 \2은 1초간 가격이 떨어지지 않았습니다.
' 5초 시점의 \3은 0초간 가격이 떨어지지 않았습니다.
=======================================================================================================================================
+ 수정 기록 +
=======================================================================================================================================
' 2021-03-02-Tue : 코딩 시작
                   주석 처리 -> 보완 필요
                   Test 7 오류 -> 보완 필요
                   효율성 테스트 0점 -> 보완 필요
=======================================================================================================================================
'''

prices = [1, 2, 3, 2, 3]  # 주식 가격
answer = []  # 결과 배열

temp = prices[:] # j for문에서 사용할 배열
del temp[0] # j는 i보다 다음 원소를 가리켜야하므로 리스트의 0번째 원소 삭제
for i in prices: # i = 주식 가격

    if prices.index(i) == len(prices): # i의 index 번호가 prices 배열의 길이와 같으면
        answer.insert(i, 0) # 해당 index의 값을 0으로 바꾸고
        break # for문 나감

    count = 0
    for j in temp:
        if i <= j: # i가 j 보다 작거나 같으면 가격이 떨어지지 않았기 때문에
            count += 1 # count 증가

        else:
            count += 1
            break
    if(len(temp)):
        del temp[0]
    answer.append(count)

print(answer)
