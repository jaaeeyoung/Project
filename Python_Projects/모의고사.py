def solution(answers):
    answer = []
    s1 = [1, 2, 3, 4, 5]
    s2 = [2, 1, 2, 3, 2, 4, 2, 5]
    s3 = [3, 3, 1, 1, 2, 2, 4, 4, 5, 5]
    
    # 수포자 정답 채우기
    i = 0
    j = 0
    k = 0
    while True:
        if len(s1) < len(answers):
            s1.append(s1[i])
            i += 1
            if i == 5:
                i = 0
        if len(s2) < len(answers):
            s2.append(s2[j])
            j += 1
            if j == 8:
                j = 0
        if len(s3) < len(answers):
            s3.append(s3[k])
            k += 1
            if k == 10:
                k = 0
        if len(s1) >= len(answers) and len(s2) >= len(answers) and len(s3) >= len(answers):
            break

    # 정답 개수 확인
    count = [0 for i in range(3)]
    for i in range(len(answers)):
        if answers[i] == s1[i]:
            count[0] += 1
        if answers[i] == s2[i]:
            count[1] += 1
        if answers[i] == s3[i]:
            count[2] += 1

    # Max 확인
    max_ = max(count)
    for i in range(len(count)):
        if max_ == count[i]:
            answer.append(i+1)
    answer.sort()
    return answer

print(solution([1,2,3,4,5])) # [1]
print(solution([1,3,2,4,2])) # [1,2,3]